const supportedLocales = ['en', 'de', 'fr', 'es']

function normalizeLocale(locale = 'en') {
  return supportedLocales.includes(locale) ? locale : 'en'
}

function localizeGame(locale = 'en') {
  const normalizedLocale = normalizeLocale(locale)
  return {
    ...sharedInsideTheBackrooms,
    ...localizedGames[normalizedLocale],
  }
}

export function getRelatedGames(locale = 'en') {
  return [localizeGame(locale)]
}

export function getRelatedGamesPageCopy(locale = 'en') {
  return pageCopy[normalizeLocale(locale)]
}

export const relatedGames = getRelatedGames('en')

export function findRelatedGameByAddress(addressBar, locale = 'en') {
  return getRelatedGames(locale).find((game) => game.addressBar === addressBar)
}

export function getAdjacentRelatedGames(currentGame, locale = 'en') {
  const games = getRelatedGames(locale)
  const index = games.findIndex((game) => game.id === currentGame?.id)

  return {
    prev: index > 0 ? games[index - 1] : null,
    next: index >= 0 && index < games.length - 1 ? games[index + 1] : null,
  }
}
